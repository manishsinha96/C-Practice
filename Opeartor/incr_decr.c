#include<stdio.h>

int main()
{
    int a = 10;

   printf("value of a is %d\n",a);
    printf("value of a post increment = %d\n",a++);
    printf("value of a after post increment  is %d\n",a);
    
    printf("value of a is %d\n",a);
    printf("value of a pre increment = %d\n",++a);
    printf("value of a after post increment  is %d\n",a);



    printf("value of a is %d\n",a);
    printf("value of a post decrement = %d\n",a--);
    printf("value of a after post decrement  is %d\n",a);
    
    printf("value of a is %d\n",a);
    printf("value of a pre decrement = %d\n",--a);
    printf("value of a after post decrement  is %d\n",a);



    return 0;
}